-- =============================================
-- Author:		Stephanie Barahona
-- Create date: 01-09-2015
-- Description:	Obtiene el estado de operación SUGEF de acuerdo al rango de días de la fuente
-- =============================================
CREATE FUNCTION [dbo].[Obtiene_Rango_Estado] (@pDias int, @pEstadoEntidad int)
RETURNS int
AS
BEGIN
	DECLARE @vsEstadoSUGEF INT;

	SET @vsEstadoSUGEF = 
		(SELECT TOP 1 COD_TIPO_ESTADO_OPERACION_SUGEF
			--, CASE WHEN 
		FROM SICAD_CONFIGURACION.dbo.HML_TIPO_ESTADO_OPERACION
		WHERE @pEstadoEntidad = COD_TIPO_ESTADO_OPERACION_ENTIDAD
			AND (CASE WHEN RANGO_ATRASO = 0 THEN 0 ELSE @pDias END) <= RANGO_ATRASO
		ORDER BY  COD_TIPO_ESTADO_OPERACION_SUGEF ASC)

	-- Return the result of the function
	RETURN @vsEstadoSUGEF;

END


